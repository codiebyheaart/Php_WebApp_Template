                             <div class="default-text-block">
                                
                                <p>Phuntainment-Portal-in-a-Box,is an advanced content 
								delivery system for people without having to pay for Internet usage.
								It allows users to access a world of multimedia content such as – Video lectures, 
								Government messages, Movies, Videos, TV Serials, film documentaries, video songs,
								news, sports videos, cartoons and devotional songs, etc., directly to the users own
								-Smartphone, Tablet or Laptop when connected to Phuntainmenthotspot.  
								All the information is transferred wirelessly to be displayed on the viewers’
								devices, allowing them to enjoy a unique and outstanding experience, at absolutely 
								zero cost, without having to incur on their Internet data usage charges.  
																			</p>
																			
																			
							<p><b><span style="color:#00b894;">We are all aware that Internet data usage is expensive and usually not
							quantifiable by user. Also the quality of data services is also patchy. 

This leads for us to empower the citizen to be able to get access to infotainment without having to rely on data cost and poor service 
and hence Phuntainment.</span></b>
</p>																
																											
																			
									<br>
									<br>
									
									<p>
				<b><span style="color:#e84393;">Phuntainment-</span></b> is a very useful solution to provide infotainment in areas where people 
				have to wait for either government service delivery or at a meeting point.
				These people at the waiting areas are the captive audience which:<br>

a)	Have nothing to pass their time till their turn comes or they get the service they are waiting for,<br>
b)	Usually can not afford to pay for data services on their mobiles,<br>
c)	Quality of data signal is poor.<br>
d)	Given a choice they will connect their phone to get free infotainment.<br>

																	
									</p>
								<p>
								
								
People in above category become captive audience, which means that they can be 
pushed information when they are watching entertainment and thus building an image of the 
brand in their minds without them realizing.<br>

The brand could be of the Government, Chief Minister or a Minister, including the development work that the
 government is doing or it could be informative on various schemes and services that the government is offering to 
 the citizens.<br>

While this captive audience is watching entertainment, information on good governance can be fed into them by the 
medium of video inserts in the entertainment. Visual inputs have a far greater and lasting impression on the minds
 of people and can thus help political parties retain the power.<br>

								
								</p>	
								<p>
<b><span style="color:#e84393;">Phuntainment-</span></b> is an excellent medium to be in touch with your citizens.<br>

Possible places Phuntainment can be deployed for the benefit of citizens:<br>

a)	School and colleges for e-learning <br>
b)	Hospital OPDs and inpatient attendant waiting areas<br>
c)	Panchayats for rural entertainment<br>
d)	MLA, Ministers and Chief Ministers waiting areas<br>
e)	Driving License offices<br>
f)	Toll Tax and State borders<br>

								
								
